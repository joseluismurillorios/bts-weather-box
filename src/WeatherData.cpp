uint8_t * WeatherData::GetIcon(String weatherIcon) {
  if (weatherIcon == "01d") {
    return sun;
  } else if (weatherIcon == "01n") {
    return moon;
  } else if (weatherIcon == "02d") {
    return partlyCloudyDay;
  } else if (weatherIcon == "02n") {
    return partlyCloudyNight;
  } else if (weatherIcon == "03d" || weatherIcon == "03n" || weatherIcon == "04d" || weatherIcon == "04n") {
    return cloud;
  } else if (weatherIcon == "09d" || weatherIcon == "09n" || weatherIcon == "10d" || weatherIcon == "10n") {
    return rain;
  } else if (weatherIcon == "11d" || weatherIcon == "11n") {
    return thunderstorm;
  } else if (weatherIcon == "50d") {
    return haze;
  } else if (weatherIcon == "50n") {
    return fog;
  } else {
    return puzzled; // no icon found
  }
}
